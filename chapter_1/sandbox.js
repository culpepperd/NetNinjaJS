// switch statements
const grade = 'A';

switch(grade){
    case 'A':
        console.log("You got an A!");
        break;
    case 'B':
        console.log("You got an B!");
        break;
    case 'C':
        console.log("You got an C!");
        break;
    case 'D':
        console.log("You got an D!");
        break;
    case 'F':
        console.log("You got an F!");
        break;
    default:
        console.log("Not a vaild grade");
}

